// Strategy: identify FR string regions (titleFr/excerptFr/contentFr) and apply replacements only within them.
// We process line-by-line and track context.

const lines = src.split("\n");
let inContentFrArray = false;
let outLines = [];
let totalReplacements = 0;
let touchedLines = 0;

function applyReplacements(line) {
  let out = line;
  let count = 0;
  for (const [from, to] of REPLACEMENTS) {
    if (from === to) continue;
    // Word-boundary safe replace, case-sensitive
    // Escape special regex chars in `from`
    const escaped = from.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const pattern = new RegExp(`\\b${escaped}\\b`, "g");
    const before = out;
    out = out.replace(pattern, to);
    if (out !== before) count += 1;
  }
  return { line: out, count };
}

